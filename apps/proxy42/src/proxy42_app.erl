-module(proxy42_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
  vegur:start_http(8080, proxy42_router, [{middlewares, vegur:default_middlewares()}]),
  proxy42_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
  ok.

